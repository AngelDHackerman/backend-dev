// Rotated Matrix Challenge
// Write a TypeScript function that takes a 2D matrix (an array of arrays) 
// as input and returns the matrix rotated 90 degrees clockwise.

// Input:
// A 2D matrix
// [
//   [1, 2, 3],
//   [4, 5, 6],
//   [7, 8, 9]
// ]

// Expected Output:
// The matrix rotated 90 degrees clockwise:
// [
//   [7, 4, 1],
//   [8, 5, 2],
//   [9, 6, 3]
// ]